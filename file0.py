a = int(input("Enter a number: "))
b = int(input("Enter a number: "))
operation = input("Enter an operation: ")
if operation == "+":
    print(a + b)
elif operation == "-":
    print(a - b)
elif operation == "*":
    print(a * b)
elif operation == "/":
    print(a / b)
else:
    print("Invalid operation")
    